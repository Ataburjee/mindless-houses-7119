let midProducts = (data) => {

    let cont = document.getElementById("mid_grid")
    cont.innerHTML = null
    data.forEach( (el) => {


        let div = document.createElement('div')
        div.addEventListener("click",function(){
            localStorage.setItem("pro_data",JSON.stringify(el))
            let x = JSON.parse(localStorage.getItem("pro_data"))
            console.log(x)
            window.location.href = "../pro_page/pro_page.html"
        })

        let img = document.createElement('img')
        img.style.cursor = 'pointer'
        
        img.src = el.image

        let name = document.createElement('p')
        name.innerText = `Name : ${el.name}`

        let brand = document.createElement('p')
        brand.innerText = `Brand : ${el.brand}`

        let price = document.createElement('p')
        price.innerText = `Price : $ ${el.price}`

        div.append(img, name, brand, price)
        cont.append(div)
    })
}

midProducts(prArr)

function lthFun(){
    let filter = prArr.sort(function(a,b){
        return a.price < b.price ? -1 : 1;
    })
    
    midProducts(filter)
    
}

function htlFun(){
    let filter = prArr.sort(function(a,b){
        return a.price < b.price ? 1 : -1;
    })
    
    midProducts(filter)
}

