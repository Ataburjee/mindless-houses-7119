// SORTING FUNCTIONS
let arrivals = () => {
    bagData.sort(function (a, b) {
        if (a.title > b.title) return 1;
        if (a.title < b.title) return -1;
        return 0;
    })
    displayData(bagData);
}

let trending = () => {
    bagData.sort(function (a, b) {
        if (a.title > b.title) return -1;
        if (a.title < b.title) return -1;
        return 0;
    })
    displayData(bagData);
}

let lth = () => {
    bagData.sort(function (a, b) {
        if (a.price > b.price) return 1;
        if (a.price < b.price) return -1;
        return 0;
    })
    displayData(bagData);
}

let htl = () => {
    bagData.sort(function (a, b) {
        if (a.price > b.price) return -1;
        if (a.price < b.price) return 1;
        return 0;
    })
    displayData(bagData);
}

// DISPLAY AREA


let displayData = () => {
    let container = document.querySelector("#mid");
    container.innerHTML = null;

    bagData.forEach((el) => {


        let box = document.createElement("div");
        box.setAttribute("class", "card")

        let title = document.createElement("h5");
        title.innerText = el.title;

        let detail = document.createElement("h5");
        detail.innerText = el.detail;

        let price = document.createElement("p");
        price.innerText = `$ ${el.price}`;

        let image = document.createElement("img");
        image.src = el.image;

        let imgbox = document.createElement("div");
        imgbox.append(image);
        imgbox.setAttribute("class", "imgbox")

        box.append(imgbox, title, detail, price);
        container.append(box);

    })
}

displayData(bagData);